package dto;

public class Tdto {
	private String name, area;
	private int age;
	
	public Tdto(String name, String area, int age) {
		this.name = name;
		this.area = area;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public String getArea() {
		return area;
	}

	public int getAge() {
		return age;
	}
	
	

}
